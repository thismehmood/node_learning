//validator library file add from node modules
const validator = require('validator');
//print validator output returns true or false
console.log(validator.isEmail('mehmood@gmail.com'));
//returns url true or false
console.log(validator.isURL('https://andewhead.io'));
//print validator output is 'true' , all characters are in uppercase
console.log(validator.isUppercase('NOONE IS BETTER'));
//print validator ouput is 'false', all characters are not uppercase
console.log(validator.isUppercase('something is better than nothing'));
console.log(validator.isVariableWidth('mehmosodnscsncj'))
//print lower case 
console.log(validator.isLowercase('somthing is beter than nono thing'));